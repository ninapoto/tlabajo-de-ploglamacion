# Operdaor Y o AND, es V cuando ambos valores son V
print("Operador Y")
print(True & True)
print(True & False)
print(False & True)
print(False & False)

# Operdaor O u OR, es V cuando al menos 1 valor es V
print("Operador O")
print(True | True)
print(True | False)
print(False | True)
print(False | False)

# Negación NOT
print("Negación, NOT")
print(not True)
print(not False)