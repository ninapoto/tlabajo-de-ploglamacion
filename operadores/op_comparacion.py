variable_1 = 6

#OPERADOR EQUIVALENTE A
print("operador equivalente")
print(variable_1 == 6)
print(variable_1 == 9)

#OPERADOR DISTINTO DE
print("operador distinto")
print(variable_1 != 6)
print(variable_1 != 8)

#OPERADOR MAYOR QUE
print("operador mayor que")
print(variable_1 > 8)
print(variable_1 > 4)
#OPERADOR MENOR QUE
print("operador menor que")
print(variable_1 < 4)
print(variable_1 < 7)

#OPERADOR MAYOR O IGUAL QUE
print("operador mayor o igual que")
print(variable_1 >= 8)
print(variable_1 >= 6)
#OPERADOR MENOR O IGUAL QUE
print("operador menor o igual que")
print(variable_1 <= 6)
print(variable_1 <= 4)

